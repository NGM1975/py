def sumar(op1,op2):
    print("El resultado de sumar es: ", op1 + op2)

def restar(op1,op2):
    print("El resultado de restar es: ",op1 - op2)

def multiplicar(op1,op2):
    print("El resultado de multiplicar es: ",op1 * op2)

def dividir(op1,op2):
    print("El resultado de dividir es: ",op1 / op2)

def redondear(op1):
    print("El resultado de redondear es: ", round(op1))

def potencia(base,exponente):
    print("El resultado de la potencia es: ", base ** exponente)